(function () {
  function esc(value) {
    return String(value ?? '')
      .replace(/&/g, '&amp;')
      .replace(/</g, '&lt;')
      .replace(/>/g, '&gt;')
      .replace(/"/g, '&quot;')
      .replace(/'/g, '&#039;');
  }

  function filenameSafe(value) {
    return String(value || 'AFS-Pack')
      .replace(/[^a-z0-9\- ]/gi, '')
      .replace(/\s+/g, '-')
      .slice(0, 80) || 'AFS-Pack';
  }

  function entityName(entity) {
    return entity?.entityName || 'Client Entity';
  }

  function periodLabel(entity) {
    return entity?.fiscalYearEnd || new Date().toLocaleDateString();
  }

  function buildClientReadyHtml(entity, packText, meta = {}) {
    const title = `${entityName(entity)} - Annual Financial Statements`;
    const lines = String(packText || '').split('\n');
    const htmlBody = lines.map((line) => {
      const clean = esc(line);
      if (/^(NOTE \d+|STATEMENT|DRAFT ANNUAL|TAX RECONCILIATION|NOTES TO FINANCIAL STATEMENTS|AUDITED FINANCIAL STATEMENTS|ANNUAL FINANCIAL STATEMENTS)/i.test(line.trim())) {
        return `<h2>${clean}</h2>`;
      }
      if (/^={8,}$/.test(line.trim())) return '<hr />';
      if (!line.trim()) return '<br />';
      return `<p>${clean}</p>`;
    }).join('\n');

    return `<!DOCTYPE html>
<html>
<head>
<meta charset="UTF-8" />
<title>${esc(title)}</title>
<style>
  @page { size: A4; margin: 22mm 18mm; }
  body { font-family: Arial, Helvetica, sans-serif; color: #111827; line-height: 1.45; }
  .cover { height: 88vh; display: flex; flex-direction: column; align-items: center; justify-content: center; text-align: center; border: 2px solid #1a4d8c; padding: 32px; }
  .cover h1 { font-size: 24px; margin: 0 0 16px; text-transform: uppercase; }
  .cover h2 { font-size: 18px; margin: 0 0 8px; color: #1a4d8c; }
  .cover p { margin: 4px 0; }
  .page-break { page-break-after: always; }
  h2 { font-size: 16px; color: #1a4d8c; margin-top: 22px; border-bottom: 1px solid #d1d5db; padding-bottom: 4px; text-transform: uppercase; }
  p { margin: 3px 0; white-space: pre-wrap; }
  hr { border: none; border-top: 2px solid #1a4d8c; margin: 18px 0; }
  .footer { margin-top: 40px; font-size: 11px; color: #6b7280; border-top: 1px solid #d1d5db; padding-top: 8px; }
</style>
</head>
<body>
  <section class="cover">
    <h1>${esc(entityName(entity))}</h1>
    <h2>Annual Financial Statements</h2>
    <p>For the period ended ${esc(periodLabel(entity))}</p>
    <p>${esc(meta.frameworkName || 'PFRS')}</p>
    <p>Client-ready draft package</p>
  </section>
  <div class="page-break"></div>
  ${htmlBody}
  <div class="footer">Generated by PFRS Expert PWA. Draft for management review and auditor validation.</div>
</body>
</html>`;
  }

  function downloadText(filename, content, mime) {
    const blob = new Blob([content], { type: mime || 'text/plain;charset=utf-8' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = filename;
    document.body.appendChild(a);
    a.click();
    a.remove();
    setTimeout(() => URL.revokeObjectURL(url), 1000);
  }

  function exportHtml(entity, packText, meta) {
    const html = buildClientReadyHtml(entity, packText, meta);
    downloadText(`${filenameSafe(entityName(entity))}-AFS-Pack.html`, html, 'text/html;charset=utf-8');
    return html;
  }

  function exportWordCompatible(entity, packText, meta) {
    const html = buildClientReadyHtml(entity, packText, meta);
    downloadText(`${filenameSafe(entityName(entity))}-AFS-Pack.doc`, html, 'application/msword;charset=utf-8');
    return html;
  }

  function exportTextPack(entity, packText) {
    downloadText(`${filenameSafe(entityName(entity))}-AFS-Pack.txt`, packText, 'text/plain;charset=utf-8');
  }

  function buildEafsChecklist(entity, meta = {}) {
    return [
      `${entityName(entity)}`,
      'SEC eAFS PACKAGE CHECKLIST',
      `Period: ${periodLabel(entity)}`,
      '',
      '[ ] Audited Financial Statements with Notes',
      '[ ] Statement of Management Responsibility, if required/applicable',
      '[ ] Independent Auditor’s Report',
      '[ ] Income Tax Return and required attachments, if applicable',
      '[ ] SEC cover sheet / general information reference, if applicable',
      '[ ] Board approval or management authorization evidence',
      '[ ] Supporting schedules for material notes',
      '',
      'FILE NAMING GUIDE',
      `AFS_${filenameSafe(entityName(entity))}_${periodLabel(entity).replace(/[^0-9A-Za-z]/g, '')}.pdf`,
      `ITR_${filenameSafe(entityName(entity))}_${periodLabel(entity).replace(/[^0-9A-Za-z]/g, '')}.pdf`,
      '',
      'SYSTEM NOTE',
      'This checklist is a packaging aid only. Final SEC eAFS submission requirements must be validated against the latest SEC and engagement-specific requirements.'
    ].join('\n');
  }

  window.CAPO_EXPORT_ENGINE = {
    buildClientReadyHtml,
    exportHtml,
    exportWordCompatible,
    exportTextPack,
    buildEafsChecklist
  };
})();